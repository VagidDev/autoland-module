    private void uploadComboBox() {
        List<String> bodyTypes = automobileController.getBodyTypes();
        bodyTypes.forEach(e -> bodyComboBox.addItem(e));
    }

    private void setValues() {
        markTextField.setText(automobile.getMark());
        modelTextField.setText(automobile.getModel());
        bodyComboBox.setSelectedItem(automobile.getBodyType());
        placeTextField.setText(String.valueOf(automobile.getPlaceCount()));
        prodTextField.setText(String.valueOf(automobile.getProdYear()));
        photoTextField.setText(automobile.getImagePath());
    }


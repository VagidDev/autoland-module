    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        boolean isProcessed = false;
        String processedText = "";

        String mark = markTextField.getText();
        String model = modelTextField.getText();
        String bodyType = bodyComboBox.getSelectedItem().toString();
        String placeCount = placeTextField.getText();
        String productionYear = prodTextField.getText();
        String imagePath = photoTextField.getText();

        if (automobile == null) {
            isProcessed = automobileController.saveAutomobile(mark, model, bodyType, placeCount, productionYear, imagePath);
            processedText = "Automobile was added!";
        } else {
            isProcessed = automobileController.updateAutomobile(automobile, mark, model, bodyType, placeCount, productionYear, imagePath);
            processedText = "Automobile was updated!";
        }

        if (isProcessed) {
            JOptionPane.showMessageDialog(this, processedText, "Success", JOptionPane.INFORMATION_MESSAGE);
            lastForm.showWindow();
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Some error with this statemnet", "Error", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_saveButtonActionPerformed

    private void photoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_photoButtonActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();

        fileChooser.setDialogTitle("Choose image of automobile");

        int userSelection = fileChooser.showOpenDialog(this);

        if (userSelection == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            photoTextField.setText(selectedFile.getAbsolutePath().replace('\\', '/'));
        }
    }//GEN-LAST:event_photoButtonActionPerformed


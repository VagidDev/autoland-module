    private void setTitle() {
        markLabel.setText(automobile.getMark());
        modelLabel.setText(automobile.getModel());
    }

    private void uploadComboBox() {

        equipmentController.getEngineTypes().forEach(e -> engTypeComboBox.addItem(e));
        equipmentController.getSuspensionTypes().forEach(e -> suspTypeComboBox.addItem(e));
        equipmentController.getDriveTypes().forEach(e -> driveTypeComboBox.addItem(e));
        equipmentController.getGearboxTypes().forEach(e -> gearTypeComboBox.addItem(e));
        equipmentController.getFuelTypes().forEach(e -> fuelTypeComboBox.addItem(e));
    }
    
    private void setValues() {
        nameTextField.setText(equipment.getName());
        engTextField.setText(equipment.getEngineName());
        engTypeComboBox.setSelectedItem(equipment.getEngineType());
        engVolTextField.setText(String.valueOf(equipment.getEngineVolume()));
        horseTextField.setText(String.valueOf(equipment.getHorsepower()));
        suspTypeComboBox.setSelectedItem(equipment.getSuspensiveType());
        driveTypeComboBox.setSelectedItem(equipment.getDriveType());
        gearTypeComboBox.setSelectedItem(equipment.getGearboxType());
        speedTextField.setText(String.valueOf(equipment.getSpeedCount()));
        fuelTypeComboBox.setSelectedItem(equipment.getFuelType());
        interTextField.setText(equipment.getInterior());
        bodyTextField.setText(equipment.getBodyKit());
        weightTextField.setText(String.valueOf(equipment.getWeigth()));
        priceTextField.setText(String.valueOf(equipment.getPrice()));
    }


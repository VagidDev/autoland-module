    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        String name = nameTextField.getText();
        String engineName = engTextField.getText();
        String engType = engTypeComboBox.getSelectedItem().toString();
        String engVol = engVolTextField.getText();
        String horsepower = horseTextField.getText();
        String suspType = suspTypeComboBox.getSelectedItem().toString();
        String driveType = driveTypeComboBox.getSelectedItem().toString();
        String gearboxType = gearTypeComboBox.getSelectedItem().toString();
        String speedCount = speedTextField.getText();
        String fuelType = fuelTypeComboBox.getSelectedItem().toString();
        String interior = interTextField.getText();
        String bodyKit = bodyTextField.getText();
        String weight = weightTextField.getText();
        String price = priceTextField.getText();
        
        boolean isProcessed;
        String processedText;

        if (equipment == null) {
            isProcessed = equipmentController.saveEquipment(automobile, name, engineName, engType, engVol, horsepower, suspType, driveType, gearboxType, speedCount, fuelType, interior, bodyKit, weight, price);
            processedText = "Equipment for '" + automobile.getMark() + " " + automobile.getModel() + "' was added!";
        } else {
            isProcessed = equipmentController.updateEquipment(equipment, name, engineName, engType, engVol, horsepower, suspType, driveType, gearboxType, speedCount, fuelType, interior, bodyKit, weight, price);
            processedText = "Equipment for '" + automobile.getMark() + " " + automobile.getModel() + "' was updated!";
        }
        
        if (isProcessed) {
            JOptionPane.showMessageDialog(this, processedText, "Confirmation", JOptionPane.INFORMATION_MESSAGE);
            this.lastForm.showWindow();
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Some error", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_saveButtonActionPerformed

